package if_else;

public class Lab4{

    public static void main(String[]args)
    {
        int a=8;

        switch(a)
        {

            case 1:
                System.out.println("case 1 :");
                break;

            case 8:
                System.out.println("case 8 :");
                break;

            case 3:
                System.out.println("case 3 :");
                break;

            default:
                System.out.println("default is running:");
                break;



        }


    }
}
